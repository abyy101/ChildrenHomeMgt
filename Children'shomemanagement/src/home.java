private JFreeChart createStudentChart() {
    JFreeChart barChart = ChartFactory.createBarChart(
            "RESOURCES CHART",
            "Children's home name",
            "Cost",
            createDataset(),
            PlotOrientation.VERTICAL,
            true, true, false);

    return barChart;
}
private CategoryDataset createDataset() {
    DefaultCategoryDataset dataset = new DefaultCategoryDataset();

    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/childrenshome", "root", null);
        st = con.prepareStatement("SELECT name, cost FROM products");

        ResultSet resultSet = st.executeQuery();

        while (resultSet.next()) {
            String name = resultSet.getString("name");
            int cost = resultSet.getInt("cost");
           
             dataset.addValue(cost, "cost", name);
             
          
        }} catch (SQLException ex) {
        
Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dataset;
    }
