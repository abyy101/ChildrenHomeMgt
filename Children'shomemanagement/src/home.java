    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed


        //for the report
                                                    
    try {
        String query = "SELECT " +
                "products.`pname` AS products_pname, " +
                "products.`cost` AS products_cost, " +
                "products.`name` AS products_name, " +
                "products.`areacode` AS products_areacode " +
                "FROM `products` products";

        // Create a JRDesignQuery
        JRDesignQuery updateQuery = new JRDesignQuery();
        updateQuery.setText(query);

        System.out.println("Loading JasperDesign...");
        // Load the JRXML file
        JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\wambu\\OneDrive\\Documents\\NetBeansProjects\\Children'shomemanagement\\src\\report2.jrxml");

        // Set the query to the JasperDesign
        jdesign.setQuery(updateQuery);

        System.out.println("Compiling JasperReport...");
        // Compile the JasperReport
        JasperReport jreport = JasperCompileManager.compileReport(jdesign);

        System.out.println("Filling JasperPrint...");
        // Fill the JasperPrint
        JasperPrint jprint = JasperFillManager.fillReport(jreport, null, con);

        System.out.println("Displaying JasperViewer...");
        // Display the JasperViewer
        JasperViewer.viewReport(jprint, false);
        System.out.println("JasperViewer displayed.");
    } catch (JRException ex) {
        Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
    }


       
    }//GEN-LAST:event_jButton5ActionPerformed

